package SafeCarSenario_Omada_4;

public class Oxhma {
	private String ar_kykloforias;
	private String marka;
	private String montelo;
	private double kyvika_ekatosta;
	private String xrwma;
	private Pelaths pelatis_;
	
	public Oxhma(String ar_kykloforias, String marka, String montelo, double kyvika_ekatosta, String xrwma,
			Pelaths pelatis_) {
		super();
		this.ar_kykloforias = ar_kykloforias;
		this.marka = marka;
		this.montelo = montelo;
		this.kyvika_ekatosta = kyvika_ekatosta;
		this.xrwma = xrwma;
		this.pelatis_ = pelatis_;
	}
	public String getAr_kykloforias() {
		return ar_kykloforias;
	}
	public void setAr_kykloforias(String ar_kykloforias) {
		this.ar_kykloforias = ar_kykloforias;
	}
	public String getMarka() {
		return marka;
	}
	public void setMarka(String marka) {
		this.marka = marka;
	}
	public String getMontelo() {
		return montelo;
	}
	public void setMontelo(String montelo) {
		this.montelo = montelo;
	}
	public double getKyvika_ekatosta() {
		return kyvika_ekatosta;
	}
	public void setKyvika_ekatosta(double kyvika_ekatosta) {
		this.kyvika_ekatosta = kyvika_ekatosta;
	}
	public String getXrwma() {
		return xrwma;
	}
	public void setXrwma(String xrwma) {
		this.xrwma = xrwma;
	}
	public Pelaths getPelatis_() {
		return pelatis_;
	}
	public void setPelatis_(Pelaths pelatis_) {
		this.pelatis_ = pelatis_;
	}

	
}