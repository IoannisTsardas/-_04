package SafeCarSenario_Omada_4;

public class Ypallhlos {
	private String onoma;
	private String epitheto;
	private String ar_mitroou;
	private String aMA;
	private String aMKA;
	
	public Ypallhlos(String onoma, String epitheto, String ar_mitroou, String aMA, String aMKA) {
		super();
		this.onoma = onoma;
		this.epitheto = epitheto;
		this.ar_mitroou = ar_mitroou;
		this.aMA = aMA;
		this.aMKA = aMKA;
	}
	
	public String getOnoma() {
		return onoma;
	}
	public void setOnoma(String onoma) {
		this.onoma = onoma;
	}
	public String getEpitheto() {
		return epitheto;
	}
	public void setEpitheto(String epitheto) {
		this.epitheto = epitheto;
	}
	public String getAr_mitroou() {
		return ar_mitroou;
	}
	public void setAr_mitroou(String ar_mitroou) {
		this.ar_mitroou = ar_mitroou;
	}
	public String getaMA() {
		return aMA;
	}
	public void setaMA(String aMA) {
		this.aMA = aMA;
	}
	public String getaMKA() {
		return aMKA;
	}
	public void setaMKA(String aMKA) {
		this.aMKA = aMKA;
	}



}